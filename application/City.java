package application;

public class City {

	private String ime;
	private String ID;
	
	public City(String i, String id) {
		
		this.ime=i;
		this.ID=id;
	}

	public String getIme() {
		return ime;
	}

	public void setIme(String ime) {
		this.ime = ime;
	}

	public String getID() {
		return ID;
	}

	public void setID(String iD) {
		ID = iD;
	}
	
	
	
	
}